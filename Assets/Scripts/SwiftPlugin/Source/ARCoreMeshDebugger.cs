    {
        mesh = new Mesh();
        mesh.vertices = debugMesh;
        mesh.triangles = faceMeshIndices;
    }

    public void Start()
    {
        filter.sharedMesh = mesh;

        vertexMarkerTransforms = new Transform[debugMesh.Length];
        
        for (int index = 0; index < debugMesh.Length; ++index)
        {
            GameObject marker = Instantiate(vertexMarkerPrefab, this.transform);
            marker.transform.localPosition = debugMesh[index];
            vertexMarkerTransforms[index] = marker.transform;
        }
    }

    public bool ToggleMeshDebugger()
    {
        gameObject.SetActive(!gameObject.activeSelf);
        return gameObject.activeSelf;
    }

    public void RecalculateMesh()
    {
        mesh.RecalculateNormals();
        mesh.RecalculateTangents();
        mesh.RecalculateBounds();
        mesh.UploadMeshData(false);
    }

    public void SetVertices(Vector3[] vertices)
    {
        mesh.vertices = vertices;
        RecalculateMesh();
        
        for (int index = 0; index < debugMesh.Length; ++index)
        {
            vertexMarkerTransforms[index].localPosition = vertices[index];
        }
    }

    public void Update()
    {
        if (string.IsNullOrEmpty(followName))
            return;
        
        if (followObject == null)
        {
            followObject = GameObject.Find(followName);
        }

        if (followObject != null)
        {
            Vector3 newFollowPosition = followObject.transform.position;
            if (Vector3.Distance(newFollowPosition, lastFollowPosition) > 0.2f)
            {
                lastFollowPosition = newFollowPosition;
                
                Transform myTransform = transform;
                myTransform.position = followObject.transform.position;

                float eulerY = followObject.transform.rotation.eulerAngles.y;
                myTransform.rotation = Quaternion.Euler(new Vector3(0, eulerY, 0)); 
            }
        }
    }
